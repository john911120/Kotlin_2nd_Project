package ex_operator

// 기초 데이터 타입 객체( Null exception)
fun main() {
    val data3 = 1000
    val data4 = 1000
    val data5: Int? = 1000
    val data6: Int? = 1000
    println("data3 == data4 is ${data3 == data4}")
    println("data3 === data4 is ${data3 === data4}")
    println("data5 == data6 is ${data5 == data6}")
    println("data5 === data6 is ${data5 === data6}")
    
}